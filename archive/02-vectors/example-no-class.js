let x = 10;
let y = 10;

////////////////////////////////////////////////////////////////
// Setup function, called once at beginning of our app
////////////////////////////////////////////////////////////////
function setup() {
	createCanvas(windowWidth, windowHeight);
}

////////////////////////////////////////////////////////////////
// Draw function, called every frame several times per second
////////////////////////////////////////////////////////////////
function draw() {
	background("black");
	fill("white");

	// Every frame increas x and y by 1
	x += 1;
	y += 1;

	circle(x, y, 10);
}
